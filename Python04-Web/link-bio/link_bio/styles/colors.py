


from enum import Enum


class Color(Enum):
    PRIMARY = "#14A1F0"     # RGB 20, 159, 240
    SECONDARY = "#087ec4"   # RGB 8, 126, 196
    BACKGROUND = "#0C151D"  # RGB 12, 21, 29
    CONTENT = "#171F26",    # RGB 23, 31, 38
    PURPLE = "#9146ff"      # RGB 144, 70, 255


class TextColor(Enum):
    HEADER = "#F1F2F4"  # RGB 241, 242, 244
    BODY = "#C3C7CB"    # RGB 195, 199, 203
    FOOTER = "#A3ABB2"  # RGB 163, 171, 178